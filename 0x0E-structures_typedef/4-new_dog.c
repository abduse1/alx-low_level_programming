#include "dog.h"

/**
 * new_dog - new attributes to a dog
 * @name: name of the dog
 * @age: age of the dog
 * @owner: owner of the dog
 *
 * Return: always nothing
 */
dog_t *new_dog(char *name, float age, char *owner)
{
	if (new_dog != NULL)
	{
		new_dog->name;
		new_dog->age;
		new_dog->owner;
	}
}
